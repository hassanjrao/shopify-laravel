     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required',
            'email'=>'required|email',
            'phone'=>'required',
            'comment'=>'required'
        ]);

        $user=User::find(1);

        $user->notify(new ContactUsNotification($request->name,$request->email,$request->phone,$request->comment));

        return back()->withToastSuccess('Your message has been sent successfully, we will get back to you soon');
    }

